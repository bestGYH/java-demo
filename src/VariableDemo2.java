public class VariableDemo2 {
    public static void main(String[] args) {
        // long类型值后面加L
        long l = 100000000000L;
        System.out.println(l);
        /// float类型值后面加F
        float f= 10.3F;
        System.out.println(f);
        /*
            变量在使用的时候必须有值
            变量有作用域
            一行可定义多个变量，但不建议
         */
        int a = 1,b = 2,c = 4;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
